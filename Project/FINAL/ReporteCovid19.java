
public class ReporteCovid19{
	
	private String persona;
	private String fechaDePrueba;

	public ReporteCovid19(String persona, String fechaDePrueba) {
		super();
		this.persona = persona;
		this.fechaDePrueba = fechaDePrueba;
	}

	public String getPersona() {
		return persona;
	}


	public String getFechaDePrueba() {
		return fechaDePrueba;
	}

	
}
